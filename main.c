/*  Main Function */
/*---------------------------------------------------------------------------------------------------------*/
int32_t main(void) {
    /* Init System, IP clock and multi-function I/O
       In the end of SYS_Init() will issue SYS_LockReg()
       to lock protected register. If user want to write
       protected register, please issue SYS_UnlockReg()
       to unlock protected register if necessary */

    /* Unlock protected registers */
    SYS_UnlockReg();

    /* Init System, IP clock and multi-function I/O */
    SYS_Init();

    /* Lock protected registers */
    SYS_LockReg();

    /* Init UART to 115200-8n1 for print message */
    UART0_Init();

    printf("\n\nCPU @ %dHz(PLL@ %dHz)\n", SystemCoreClock, PllClock);

    printf("PWM0 clock is from %s\n",
           (CLK->CLKSEL2 & CLK_CLKSEL2_PWM0SEL_Msk) ? "CPU" : "PLL");
    printf("  This sample code will output waveform with PWM0 channel 0.\n");
    printf("  PWM0 channel 0: 320000 Hz\n");
    printf("    waveform output pin: PWM0_CH0(PB.5)\n");

    /* PWM0 channel 0 frequency and duty configuration are as follows */
    PWM_ConfigOutputChannel(PWM0, 0, 320000, 0);

    /* Enable output of PWM0 channel 0 */
    PWM_EnableOutput(PWM0, 0x01);

    /* Start PWM0 counter */
    PWM_Start(PWM0, 0x01);

    /* Open Timer1 in periodic mode, enable interrupt and 8000 interrupt ticks
     * per second */
    TIMER_Open(TIMER1, TIMER_PERIODIC_MODE, sounddata_sampleRate);
    TIMER_EnableInt(TIMER1);

    /* Enable Timer1 NVIC */
    NVIC_EnableIRQ(TMR1_IRQn);

    /* Start Timer1 counting */
    TIMER_Start(TIMER1);

    while (1)
        ;
}
